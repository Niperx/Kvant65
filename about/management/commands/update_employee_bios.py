def clean_bio_html(html, name=None):
    """
    Удаляет из биографии ФИО, должность и направление; нормализует пробелы,
    переносы и пунктуацию; исправляет типичные опечатки с оригинального сайта.
    """
    from bs4 import BeautifulSoup

    if not (html or "").strip():
        return html

    # Убрать ведущую строку с ФИО (до первого <)
    if name:
        html = re.sub(r"^\s*" + re.escape(name) + r"\s*\n*", "", html, count=1, flags=re.IGNORECASE)

    soup = BeautifulSoup(html, "html.parser")

    # Удалить параграфы, которые целиком — «Должность» или «Направление»
    for tag in list(soup.find_all("p")):
        text = tag.get_text(strip=True)
        if not text:
            tag.decompose()
            continue
        lower = text.lower()
        if lower.startswith("должность") or lower.startswith("направление") or "должность и преподаваемые" in lower:
            tag.decompose()

    # Удалить блоки <strong>Должность...</strong> без обёртки <p> (только если блок небольшой)
    for strong in list(soup.find_all("strong")):
        t = strong.get_text(strip=True).lower()
        if not (t.startswith("должность") or t.startswith("направление")):
            continue
        parent = strong.parent
        if parent and parent.get_text(strip=True).lower().startswith(("должность", "направление")):
            if len(parent.get_text(strip=True)) < 200:
                parent.decompose()

    # Удалить оставшиеся пустые <p></p>
    for p in soup.find_all("p"):
        if not p.get_text(strip=True) and not p.find_all(True):
            p.decompose()

    # Убрать служебные атрибуты с <li> (например class="msg..." у Максименко)
    for li in soup.find_all("li"):
        if li.get("class") and "msg" in " ".join(li.get("class", [])).lower():
            del li["class"]
        if li.get("role"):
            del li["role"]

    result = str(soup)

    # Нормализация: пробел после знаков препинания; не более одного пробела подряд; единые переносы
    result = re.sub(r"([.,;:])([^\s<])", r"\1 \2", result)
    result = re.sub(r"[ \t]+", " ", result)
    result = re.sub(r" </p>", "</p>", result)
    result = re.sub(r"<p> ", "<p>", result)
    result = re.sub(r"\n{3,}", "\n\n", result)
    result = re.sub(r" ?\n ?", "\n", result)
    result = result.strip()

    # Типичные опечатки с сайта
    fixes = [
        (r"общесмтве6нной", "общественной"),
        (r"Архипилаг", "Архипелаг"),
        (r"Oppjrtunities", "Opportunities"),
        (r"творчесва", "творчества"),
        (r"облучающегося", "обучающегося"),
        (r"Роботехника", "Робототехника"),
        (r"Роботехническ", "Робототехническ"),
        (r"на освоение  дополнительной", "на освоение дополнительной"),
        (r"Педагогический VIP молодежь", "Педагогический вызов: молодёжь"),
        (r"350км\\\\ч", "350 км/ч"),
        (r"350км/ч", "350 км/ч"),
        (r"Нетодология", "Методология"),
        (r"стендап- конкурс", "стендап-конкурс"),
        (r"сироти детей", "сирот и детей"),
        (r"Квант-Нерегионального", "Квант-ИН регионального"),
    ]
    for pattern, repl in fixes:
        result = re.sub(pattern, repl, result, flags=re.IGNORECASE)

    return result


def ul(*items):
    """Создаёт HTML-список из строк."""
    lines = "".join(f"<li>{i}</li>" for i in items if i)
    return f"<ul>{lines}</ul>"


def section(title, *items):
    """Раздел с заголовком и списком."""
    return f"<p><strong>{title}:</strong></p>" + ul(*items)


