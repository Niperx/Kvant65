class Command(BaseCommand):
    help = "Заполняет биографии сотрудников подробными данными с оригинального сайта"

    def add_arguments(self, parser):
        parser.add_argument(
            "--skip-filled",
            action="store_true",
            help="Пропустить сотрудников у которых bio уже заполнено",
        )
        parser.add_argument(
            "--from-scraped",
            action="store_true",
            help="Загрузить биографии из about/data/scraped_bios.json (после scrape_employee_bios)",
        )

    def handle(self, *args, **options):
        from about.models import EmployeePage

        skip_filled = options["skip_filled"]
        from_scraped = options["from_scraped"]

        if from_scraped:
            data_dir = Path(__file__).resolve().parent.parent.parent / "data"
            scraped_file = data_dir / "scraped_bios.json"
            if not scraped_file.exists():
                self.stderr.write(
                    "Файл %s не найден. Сначала выполните: python manage.py scrape_employee_bios" % scraped_file
                )
                return
            with open(scraped_file, "r", encoding="utf-8") as f:
                bios_source = json.load(f)
            self.stdout.write("Загружено %s биографий из %s" % (len(bios_source), scraped_file))
        else:
            bios_source = BIOS

        updated = 0
        skipped = 0
        not_found = []

        for slug, bio_html in bios_source.items():
            emp = EmployeePage.objects.filter(slug=slug).first()
            if not emp:
                not_found.append(slug)
                continue

            if skip_filled and emp.bio:
                skipped += 1
                continue

            if from_scraped:
                bio_html = clean_bio_html(bio_html, name=NAME_BY_SLUG.get(slug))
            emp.bio = bio_html
            emp.save()
            updated += 1
            self.stdout.write(f"  + {emp.slug}")

        self.stdout.write(self.style.SUCCESS(f"\nObnovleno: {updated}, propusheno: {skipped}"))

        if not_found:
            self.stdout.write(self.style.WARNING(
                f"\nNe naydeny v BD (slug): {', '.join(not_found)}"
            ))
